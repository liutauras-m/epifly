#!/usr/bin/env node
/**
 * scripts/dump-runes-status.mjs  (Phase 2.5)
 *
 * Scans every .svelte file in packages/ui and apps/* and classifies each as:
 *   runes    — uses $props() / $state / $derived / $effect (Svelte 5 runes)
 *   legacy   — uses `export let` / `$:` reactive statements / createEventDispatcher
 *   mixed    — has both (transitional file)
 *
 * Outputs a summary table to stdout and writes docs/ui-runes-inventory.md.
 *
 * Usage: node scripts/dump-runes-status.mjs [--json]
 *
 * The --json flag emits machine-readable JSON instead of the markdown table.
 * Used by the Phase 2.5 ESLint ratchet to set the --max-warnings=N floor.
 */

import { readFileSync, writeFileSync, readdirSync, statSync } from 'node:fs';
import { join, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(fileURLToPath(import.meta.url), '..', '..');
const args = process.argv.slice(2);
const jsonMode = args.includes('--json');

// ── Patterns ─────────────────────────────────────────────────────────────────

const RUNES_PATTERNS = [
  /\$props\s*\(/,
  /\$state\s*[(<]/,
  /\$derived\s*[(<]/,
  /\$effect\s*\(/,
  /\$bindable\s*\(/,
  /\$inspect\s*\(/,
];

const LEGACY_PATTERNS = [
  /\bexport\s+let\b/,
  /\$:\s/,
  /createEventDispatcher\s*\(/,
  /import\s+\{[^}]*writable[^}]*\}\s+from\s+['"]svelte\/store['"]/,
  /on:[a-zA-Z]+/,  // legacy on:event directives (not in $props)
];

function classify(content) {
  const hasRunes  = RUNES_PATTERNS.some((re) => re.test(content));
  const hasLegacy = LEGACY_PATTERNS.some((re) => re.test(content));
  if (hasRunes && hasLegacy) return 'mixed';
  if (hasRunes) return 'runes';
  if (hasLegacy) return 'legacy';
  return 'runes'; // no reactive patterns → effectively runes-compatible (static component)
}

// ── File walker ───────────────────────────────────────────────────────────────

function walkSvelte(dir, results = []) {
  let entries;
  try { entries = readdirSync(dir); } catch { return results; }
  for (const entry of entries) {
    if (entry.startsWith('.') || entry === 'node_modules') continue;
    const full = join(dir, entry);
    const stat = statSync(full);
    if (stat.isDirectory()) {
      walkSvelte(full, results);
    } else if (entry.endsWith('.svelte')) {
      const content = readFileSync(full, 'utf-8');
      results.push({ path: relative(ROOT, full), status: classify(content) });
    }
  }
  return results;
}

// ── Scan ──────────────────────────────────────────────────────────────────────

const DIRS = [
  join(ROOT, 'packages/ui/src/lib/components'),
  join(ROOT, 'packages/ui/src/lib/features'),
  join(ROOT, 'packages/ui/src/lib/utils'),
  join(ROOT, 'apps/web/src'),
  join(ROOT, 'apps/browser-shell/src'),
];

const files = DIRS.flatMap((d) => walkSvelte(d));

// Group by directory bucket
function bucket(path) {
  if (path.startsWith('packages/ui/src/lib/components')) return 'components/';
  if (path.startsWith('packages/ui/src/lib/features'))   return 'features/';
  if (path.startsWith('packages/ui/src/lib/utils'))      return 'utils/';
  if (path.startsWith('apps/web'))                       return 'apps/web/';
  if (path.startsWith('apps/browser-shell'))             return 'apps/browser-shell/';
  return 'other/';
}

const grouped = {};
for (const f of files) {
  const b = bucket(f.path);
  grouped[b] ??= { runes: 0, legacy: 0, mixed: 0, total: 0, files: [] };
  grouped[b][f.status]++;
  grouped[b].total++;
  grouped[b].files.push(f);
}

if (jsonMode) {
  console.log(JSON.stringify({ summary: grouped, files }, null, 2));
  process.exit(0);
}

// ── Markdown output ───────────────────────────────────────────────────────────

const now = new Date().toISOString().slice(0, 10);
const lines = [
  `# Svelte 5 Runes Adoption — ${now}`,
  '',
  '> Auto-generated by `node scripts/dump-runes-status.mjs`. Do not hand-edit.',
  '> Re-run after any Svelte file migration to update the ESLint `--max-warnings` baseline.',
  '',
  '## Summary',
  '',
  '| Directory | Total | Runes | Mixed | Legacy | % Runes |',
  '|---|---|---|---|---|---|',
];

let totalAll = 0, runesAll = 0, legacyAll = 0, mixedAll = 0;

for (const [dir, counts] of Object.entries(grouped)) {
  const pct = counts.total ? Math.round((counts.runes / counts.total) * 100) : 100;
  lines.push(`| \`${dir}\` | ${counts.total} | ${counts.runes} | ${counts.mixed} | ${counts.legacy} | ${pct}% |`);
  totalAll += counts.total;
  runesAll += counts.runes;
  legacyAll += counts.legacy;
  mixedAll += counts.mixed;
}

const totalPct = totalAll ? Math.round((runesAll / totalAll) * 100) : 100;
lines.push(`| **TOTAL** | **${totalAll}** | **${runesAll}** | **${mixedAll}** | **${legacyAll}** | **${totalPct}%** |`);

lines.push('', '## Phase 2.5 ratchet baseline', '');
lines.push(`The ESLint \`--max-warnings\` floor for the current commit is **${legacyAll + mixedAll}** (legacy + mixed files).`);
lines.push('Set this as the ratchet baseline in `.eslintrc` until the directory migration closes.');

if (legacyAll > 0 || mixedAll > 0) {
  lines.push('', '## Files needing migration', '');
  lines.push('| File | Status |');
  lines.push('|---|---|');
  for (const f of files.filter((f) => f.status !== 'runes')) {
    lines.push(`| \`${f.path}\` | ${f.status} |`);
  }
}

const md = lines.join('\n') + '\n';
const outPath = join(ROOT, 'docs/ui-runes-inventory.md');
writeFileSync(outPath, md, 'utf-8');

console.log(md);
console.log(`\n✓ Written to ${outPath}`);
console.log(`  Total: ${totalAll} files — ${runesAll} runes, ${mixedAll} mixed, ${legacyAll} legacy`);
